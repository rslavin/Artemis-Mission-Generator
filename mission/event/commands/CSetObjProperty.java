package mission.event.commands;

import mission.expression.Expression;

public class CSetObjProperty extends CModProperty {

	protected CSetObjProperty() {
		super("set_object_property");
	}
	
	public CSetObjProperty(Object name, String property, float val) {
		super("set_object_property");
		init(name, property, val);
	}

	public CSetObjProperty(Object name, String property, int val) {
		super("set_object_property");
		init(name, property, val);
	}
	
	public CSetObjProperty(Object name, String property, Expression val) {
		super("set_object_property");
		init(name, property, val);
	}

}
