package mission.event.conditions;

import mission.event.Condition;

public class CSphere extends Condition {

	public CSphere(String tag, Object n, int centerX, int centerY, int centerZ, int radius) {
		super(tag);
		
		put("name", n);
		put("centerX", centerX);
		put("centerY", centerY);
		put("centerZ", centerZ);
		put("radius", radius);
	}
	
}
