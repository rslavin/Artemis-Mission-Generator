package mission.event.conditions;

import mission.event.Condition;

public class CBox extends Condition {

	public CBox(String tag, Object n, int leastX, int leastZ, int mostX, int mostZ) {
		super(tag);
		
		put("name", n);
		put("leastX", leastX);
		put("leastZ", leastZ);
		put("mostX", mostX);
		put("mostZ", mostZ);
	}
	
}
